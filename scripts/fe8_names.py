def _parse(src: str, prefix: str) -> Dict[int, str]:
    out: Dict[int, str] = {}
    for name, val in re.findall(r"([A-Z0-9_]+)\s*=\s*(0x[0-9A-Fa-f]+)", src):
        label = name[len(prefix):] if name.startswith(prefix) else name
        out[int(val, 16)] = label
    return out


ITEM_NAMES: Dict[int, str] = _parse(_ITEMS_SRC, "ITEM_")
CLASS_NAMES: Dict[int, str] = _parse(_CLASSES_SRC, "CLASS_")
CHARACTER_NAMES: Dict[int, str] = _parse(_CHARACTERS_SRC, "CHARACTER_")


def item_name(index: int) -> str:
    return ITEM_NAMES.get(index, f"ITEM_{index:#04x}")


def class_name(index: int) -> str:
    return CLASS_NAMES.get(index, f"CLASS_{index:#04x}")


def character_name(index: int) -> str:
    return CHARACTER_NAMES.get(index, f"CHARACTER_{index:#04x}")


